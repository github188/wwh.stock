package com.bm.wanma.entity;

import java.io.Serializable;

/**
 * 优惠券实体类
 */
public class CouponBean implements Serializable {
	
	private String limitation;//限制 1-仅限交流电桩，2-仅限直流电装，3-不限充电桩
	private String cValue;//券面值
	private String cCondition ;//券启用金额
	private String timeout ;//过期时间
	private String rule ;//券对应的活动1-注册送现金券活动，2-首次体验享现金券，3-邀请注册返现金券活动，4-邀请首次消费返现金券活动
	private String cLabel;
	public String getLimitation() {
		return limitation;
	}
	public void setLimitation(String limitation) {
		this.limitation = limitation;
	}
	public String getcValue() {
		return cValue;
	}
	public void setcValue(String cValue) {
		this.cValue = cValue;
	}
	public String getcCondition() {
		return cCondition;
	}
	public void setcCondition(String cCondition) {
		this.cCondition = cCondition;
	}
	public String getTimeout() {
		return timeout;
	}
	public void setTimeout(String timeout) {
		this.timeout = timeout;
	}
	public String getRule() {
		return rule;
	}
	public void setRule(String rule) {
		this.rule = rule;
	}
	public String getcLabel() {
		return cLabel;
	}
	public void setcLabel(String cLabel) {
		this.cLabel = cLabel;
	}

}
