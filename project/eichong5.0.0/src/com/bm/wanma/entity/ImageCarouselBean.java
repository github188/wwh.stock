package com.bm.wanma.entity;

import java.io.Serializable;

import net.tsz.afinal.annotation.sqlite.Id;
import net.tsz.afinal.annotation.sqlite.Table;

/**
 * 图片轮播bean
 */
@Table(name = "tb_imagecarousel_data")
public class ImageCarouselBean implements Serializable{
	@Id(column="pkBlId")
	private String pkBlId;
	private String blDesc;
	private String blUrl;
	private String blStatus;
	private String blSort;
	private String blCreatedate;
	private String blUpdatedate;
	private String blBeginTime;
	private String blEndTime;
	private String blProvincecode;
	private String blCitycode;
	private String blCountycode;
	
	private String bannerPicUrl;

	public String getPkBlId() {
		return pkBlId;
	}

	public void setPkBlId(String pkBlId) {
		this.pkBlId = pkBlId;
	}

	public String getBlDesc() {
		return blDesc;
	}

	public void setBlDesc(String blDesc) {
		this.blDesc = blDesc;
	}

	public String getBlUrl() {
		return blUrl;
	}

	public void setBlUrl(String blUrl) {
		this.blUrl = blUrl;
	}

	public String getBlStatus() {
		return blStatus;
	}

	public void setBlStatus(String blStatus) {
		this.blStatus = blStatus;
	}

	public String getBlSort() {
		return blSort;
	}

	public void setBlSort(String blSort) {
		this.blSort = blSort;
	}

	public String getBlCreatedate() {
		return blCreatedate;
	}

	public void setBlCreatedate(String blCreatedate) {
		this.blCreatedate = blCreatedate;
	}

	public String getBlUpdatedate() {
		return blUpdatedate;
	}

	public void setBlUpdatedate(String blUpdatedate) {
		this.blUpdatedate = blUpdatedate;
	}

	public String getBlBeginTime() {
		return blBeginTime;
	}

	public void setBlBeginTime(String blBeginTime) {
		this.blBeginTime = blBeginTime;
	}

	public String getBlEndTime() {
		return blEndTime;
	}

	public void setBlEndTime(String blEndTime) {
		this.blEndTime = blEndTime;
	}

	public String getBlProvincecode() {
		return blProvincecode;
	}

	public void setBlProvincecode(String blProvincecode) {
		this.blProvincecode = blProvincecode;
	}

	public String getBlCitycode() {
		return blCitycode;
	}

	public void setBlCitycode(String blCitycode) {
		this.blCitycode = blCitycode;
	}

	public String getBlCountycode() {
		return blCountycode;
	}

	public void setBlCountycode(String blCountycode) {
		this.blCountycode = blCountycode;
	}

	public String getBannerPicUrl() {
		return bannerPicUrl;
	}

	public void setBannerPicUrl(String bannerPicUrl) {
		this.bannerPicUrl = bannerPicUrl;
	}

	@Override
	public String toString() {
		return "ImageCarouselBean [pkBlId=" + pkBlId + ", blDesc=" + blDesc
				+ ", blUrl=" + blUrl + ", blStatus=" + blStatus + ", blSort="
				+ blSort + ", blCreatedate=" + blCreatedate + ", blUpdatedate="
				+ blUpdatedate + ", blBeginTime=" + blBeginTime
				+ ", blEndTime=" + blEndTime + ", blProvincecode="
				+ blProvincecode + ", blCitycode=" + blCitycode
				+ ", blCountycode=" + blCountycode + ", bannerPicUrl="
				+ bannerPicUrl + "]";
	}

	
}
