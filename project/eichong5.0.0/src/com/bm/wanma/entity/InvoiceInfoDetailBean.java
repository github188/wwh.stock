package com.bm.wanma.entity;


import net.tsz.afinal.annotation.sqlite.Table;
/*开票信息 */

@Table(name = "tb_invoice_info")
public class InvoiceInfoDetailBean {
	private int id;
	private String User_ID;//用户ID
	private int Type;//类型-1是公司，2是个人
    private String CompanyName;//公司抬头
    private String TaxNum;//纳税人识别号
    private String CompanyAddr;//公司地址
    private String CompanyPhone;//公司电话
    private String CompanyBank;//开户行
    private String Bank;//对公账号
    private String Recipient;//收件人
    private String RecipientPhone;//收件人电话
    private String Pcode;
    private String Ccode;
    private String Acode;
    private String pca;
    private String RecipientAddr;//收件人地址
    
	//必须包含这个默认的构造方法，否则在进行数据查找时，会报错
	public InvoiceInfoDetailBean() {
		
	}

	
	
	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getUser_ID() {
		return User_ID;
	}

	public void setUser_ID(String user_ID) {
		User_ID = user_ID;
	}

	public int getType() {
		return Type;
	}

	public void setType(int type) {
		Type = type;
	}

	public String getCompanyName() {
		return CompanyName;
	}

	public void setCompanyName(String companyName) {
		CompanyName = companyName;
	}

	public String getTaxNum() {
		return TaxNum;
	}

	public void setTaxNum(String taxNum) {
		TaxNum = taxNum;
	}

	public String getCompanyAddr() {
		return CompanyAddr;
	}

	public void setCompanyAddr(String companyAddr) {
		CompanyAddr = companyAddr;
	}

	public String getCompanyPhone() {
		return CompanyPhone;
	}

	public void setCompanyPhone(String companyPhone) {
		CompanyPhone = companyPhone;
	}

	public String getCompanyBank() {
		return CompanyBank;
	}

	public void setCompanyBank(String companyBank) {
		CompanyBank = companyBank;
	}

	public String getBank() {
		return Bank;
	}

	public void setBank(String bank) {
		Bank = bank;
	}

	public String getRecipient() {
		return Recipient;
	}

	public void setRecipient(String recipient) {
		Recipient = recipient;
	}

	public String getRecipientPhone() {
		return RecipientPhone;
	}

	public void setRecipientPhone(String recipientPhone) {
		RecipientPhone = recipientPhone;
	}

	public String getPcode() {
		return Pcode;
	}

	public void setPcode(String pcode) {
		Pcode = pcode;
	}

	public String getCcode() {
		return Ccode;
	}

	public void setCcode(String ccode) {
		Ccode = ccode;
	}

	public String getAcode() {
		return Acode;
	}

	public void setAcode(String acode) {
		Acode = acode;
	}

	public String getRecipientAddr() {
		return RecipientAddr;
	}

	public void setRecipientAddr(String recipientAddr) {
		RecipientAddr = recipientAddr;
	}

	public String getPca() {
		return pca;
	}

	public void setPca(String pca) {
		this.pca = pca;
	}

	


  
}
