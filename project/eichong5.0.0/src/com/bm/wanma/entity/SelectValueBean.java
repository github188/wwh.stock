package com.bm.wanma.entity;

import java.io.Serializable;

/**
 * @author cm
 * 地图筛选值
 */
public class SelectValueBean implements Serializable {

	/* 快充 */
	private boolean isFast;
	/* 慢充 */
	private boolean isSlow;
	/* 国标 */
	private boolean isGuo;
	/* 欧标 */
	private boolean isOu;
	/* 空闲 */
	private boolean isIdle;
	/* 适合爱车 */
	private boolean isMatch;
	public boolean isFast() {
		return isFast;
	}
	public void setFast(boolean isFast) {
		this.isFast = isFast;
	}
	public boolean isSlow() {
		return isSlow;
	}
	public void setSlow(boolean isSlow) {
		this.isSlow = isSlow;
	}
	public boolean isGuo() {
		return isGuo;
	}
	public void setGuo(boolean isGuo) {
		this.isGuo = isGuo;
	}
	public boolean isOu() {
		return isOu;
	}
	public void setOu(boolean isOu) {
		this.isOu = isOu;
	}
	public boolean isIdle() {
		return isIdle;
	}
	public void setIdle(boolean isIdle) {
		this.isIdle = isIdle;
	}
	public boolean isMatch() {
		return isMatch;
	}
	public void setMatch(boolean isMatch) {
		this.isMatch = isMatch;
	}
	
	

}
