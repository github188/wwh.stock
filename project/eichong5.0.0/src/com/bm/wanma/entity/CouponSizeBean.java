package com.bm.wanma.entity;

import java.io.Serializable;

/**
 * 优惠券数量实体类
 */
public class CouponSizeBean implements Serializable {

	private String unusedCount;//可用
	private String usedCount;//已使用
	private String overCount;//过期
	public String getUnusedCount() {
		return unusedCount;
	}
	public void setUnusedCount(String unusedCount) {
		this.unusedCount = unusedCount;
	}
	public String getUsedCount() {
		return usedCount;
	}
	public void setUsedCount(String usedCount) {
		this.usedCount = usedCount;
	}
	public String getOverCount() {
		return overCount;
	}
	public void setOverCount(String overCount) {
		this.overCount = overCount;
	}

}
