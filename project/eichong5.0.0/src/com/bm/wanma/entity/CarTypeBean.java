package com.bm.wanma.entity;

import java.io.Serializable;

/**
 * 车型品牌实体类
 * pkParaconfig：品牌代码  
 * paraName：品牌名称
 * 
 */
public class CarTypeBean implements Serializable {

    private String pkParaconfig;//品牌代码
    private String paraName;//品牌名称

    public String getPkParaconfig() {
        return pkParaconfig;
    }

    public void setPkParaconfig(String pkParaconfig) {
        this.pkParaconfig = pkParaconfig;
    }

    public String getParaName() {
        return paraName;
    }

    public void setParaName(String paraName) {
        this.paraName = paraName;
    }
}
